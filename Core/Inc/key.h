#ifndef __KEY_H__
#define __KEY_H__

#include "main.h"

/* ================================================================
 * 按键模块 - 头文件
 * 
 * 硬件：PB12 (KEY_UP)   → 内部上拉 + 按键 → GND   （增加阈值）
 *       PB13 (KEY_DOWN) → 内部上拉 + 按键 → GND   （减少阈值）
 * 
 * 原理：按下 = 读到低电平(0)；松开 = 读到高电平(1)
 * CubeMX 已配置 GPIO_MODE_INPUT + GPIO_PULLUP（内部上拉）
 * 
 * 知识点：
 *   1. 上拉电阻的作用：让IO口在悬空时保持确定电平
 *   2. 按键消抖：机械按键需要 10~20ms 消抖时间
 *   3. 状态机：用状态记录"按下→松开"的完整动作
 * ================================================================ */

/* -------------------- 按键引脚宏 -------------------- */
#define KEY_UP_PORT     GPIOB
#define KEY_UP_PIN      GPIO_PIN_12

#define KEY_DOWN_PORT   GPIOB
#define KEY_DOWN_PIN    GPIO_PIN_13

/* -------------------- 按键返回值 -------------------- */
#define KEY_NONE    0x00   // 无按键按下
#define KEY_UP      0x01   // PB12 按下（阈值+）
#define KEY_DOWN    0x02   // PB13 按下（阈值-）

/* -------------------- 消抖时间（毫秒） -------------------- */
/*
 * 为什么是 20ms？
 * 机械按键弹跳时间通常在 5~20ms，20ms 能覆盖绝大多数情况。
 * 太短（5ms）可能消不干净 → 误触发
 * 太长（50ms）响应迟钝      → 手感差
 */
#define KEY_DEBOUNCE_MS  20

/* ================================================================
 * 按键扫描（阻塞式，适合初学者理解原理）
 * 
 * 【如何使用】
 *   在主循环中调用 Key_Scan()，它会：
 *   1. 检测按键是否按下（电平为低）
 *   2. 等 20ms 消抖后再确认
 *   3. 等按键松开后才返回结果（防重复触发）
 *   4. 返回 KEY_UP / KEY_DOWN / KEY_NONE
 * 
 * 【缺点】
 *   阻塞式的，按着键不放会让主循环卡住。
 *   后续会学非阻塞版本（配合定时器中断）
 * ================================================================ */
uint8_t Key_Scan(void);

/* ================================================================
 * 获取报警阈值（供其他模块读取）
 * 阈值默认 20cm，按键调节范围 5~100cm，步进 5cm
 * ================================================================ */
uint8_t Key_GetThreshold(void);
void    Key_Process(uint8_t key_val);

#endif /* __KEY_H__ */
